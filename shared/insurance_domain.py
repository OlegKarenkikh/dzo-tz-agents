def classify_tender(text: str) -> dict[str, Any]:
    """Full classification result for a tender document.

    Returns a dict with:
      - insurance_type: str
      - confidence: float
      - is_insurance: bool
      - type_info: dict | None (full metadata if available)
      - detected_keywords: list[str]
    """
    insurance_type, confidence = classify_insurance_type(text)
    type_info = get_insurance_type_info(insurance_type)

    # Collect detected keywords
    text_lower = text.lower() if text else ""
    detected_keywords = []
    if type_info:
        for kw in type_info.keywords:
            if kw in text_lower:
                detected_keywords.append(kw)

    result: dict[str, Any] = {
        "insurance_type": insurance_type,
        "confidence": round(confidence, 2),
        "is_insurance": is_insurance_tender(text),
        "detected_keywords": detected_keywords[:20],
    }
    if type_info:
        result["type_info"] = {
            "full_name_ru": type_info.full_name_ru,
            "abbreviation": type_info.abbreviation,
            "okpd2_codes": type_info.okpd2_codes,
            "is_mandatory": type_info.is_mandatory,
            "regulatory_base": type_info.regulatory_base,
        }
    return result
